    private void vulSpelers() {
        try {
            Sql_connect.doConnect();
            PreparedStatement stat1 = Sql_connect.getConnection().prepareStatement("select t.Id_persoon, t.Positie, p.voornaam, p.achternaam, t.Tafel_code from toernooideelnemer t join persoon p on t.Id_persoon = p.Id_persoon where Id_toernooi = ? AND t.Positie = 0 ORDER BY RAND()");
            stat1.setInt(1, whereClaus);
            ResultSet result1 = stat1.executeQuery();

            while (result1.next()) {
                ModelItem item = new ModelItem();
                item.id = result1.getInt("t.Id_persoon");
                item.voornaam = result1.getString("p.voornaam");
                item.achternaam = result1.getString("p.achternaam");
                item.positie = result1.getString("t.Positie");
                item.idTafel = result1.getString("t.Tafel_code");
                lijstModel.addElement(item);
            }
        } catch (Exception e) {
            ePopup(e);
        }
    }

    private void tellenSpelers() {
        try {
            Sql_connect.doConnect();

            PreparedStatement stat1 = Sql_connect.getConnection().prepareStatement("select count(*) as count from toernooideelnemer WHERE Id_toernooi = ? AND Positie = 0");
            stat1.setInt(1, whereClaus);
            ResultSet result1 = stat1.executeQuery();

            while (result1.next()) {
                ModelItem item = new ModelItem();
                item.positie = result1.getString("count");
                lijstModel.addElement(item);
                TXTlaagsteRating.setText(item.positie);
            }
            //vulLijst();
        } catch (Exception e) {
            ePopup(e);
        }
    }

