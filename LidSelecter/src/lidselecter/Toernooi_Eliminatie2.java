    private void gegevensLijst() {
        try {
            if (DeelnemerLijst.getSelectedValue() == null) {
                MELDINGFIELD.setText("Niks geselecteerd");

            } else {
                ModelItem selectedItem = (ModelItem) DeelnemerLijst.getSelectedValue();
                TXTspelerId.setText(Integer.toString(selectedItem.id));

                MELDINGFIELD.setText("Opvragen speler gelukt");
            }
        } catch (Exception e) {
            MELDINGFIELD.setText("Niks geselecteerd");

        }
    }

    private void update() {
        try {
            int idPersoon = Integer.parseInt(TXTspelerId.getText());
            int positie = Integer.parseInt(TXTlaagsteRating.getText());

            Sql_connect.doConnect();
            //int whereClaus = Integer.parseInt(idToernooiTxt.getText());

            PreparedStatement stat2 = Sql_connect.getConnection().prepareStatement("UPDATE toernooideelnemer SET Positie = ? WHERE Id_persoon = ? AND Id_toernooi= ?");
            stat2.setInt(1, positie);
            stat2.setInt(2, idPersoon);
            stat2.setInt(3, whereClaus);
            stat2.executeUpdate();
            
            PreparedStatement spelerAanwezigDown = Sql_connect.getConnection().prepareStatement("UPDATE tafel set spelerAanwezig=spelerAanwezig-1 where tafel_code = 1 AND Id_toernooi ?");
            //spelerAanwezigDown.setInt(1, tafelCode);
            spelerAanwezigDown.setInt(2, whereClaus);

            JOptionPane.showMessageDialog(rootPane, "Speler " + idPersoon + " is uitgechakeld en heeft rating " + positie + " meegekregen");
            Toernooi_Eliminatie2 Eliminatie2 = new Toernooi_Eliminatie2();
            Eliminatie2.setVisible(rootPaneCheckingEnabled);
            this.dispose();
            

            //vulLijst();
        } catch (Exception e) {
            ePopup(e);
        }
    }

